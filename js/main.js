// 1. if문
function ifstart() {
    let x = 10;
    
    if ( x < 0 ) { 
        console.log("x는 정수입니다.");
    }
}

// 2. if ~ else
function ifelse() {
    if ( x > 0 ) {
        console.log("x는 양수입니다.");
    }else {
        console.log("x는 음수입니다.");
    };
}

ifstart();
ifelse();

